    private void AddMoreValuesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddMoreValuesActionPerformed
        // TODO add your handling code here:
        Object[] row = new Object[5];
        Double[] limit = new Double[2];
        Double upper = null , lower = null;
        Double f = null;
        Double x = null;
        
        
        for(int i = 0 ; i < number_of_classes ; i++ ){
            //ask if open ended or not
            int n = -1;
            
            if(i==0){
                do{
                    n = OpenEndedOrNot();
                }while(n==-1);
                
                //Closed ended get first and second value
                if(n==1){
                    limit = inputClosedClassLimit();
//                    System.out.println("limit[0]" + limit[0]);
//                    System.out.println("limit[1]" + limit[1]);
                    //classInterval.add(limit);
                }
                
                //Open ended first
                else{
                    openFirst = true;
                    upper = Double.valueOf(JOptionPane.showInputDialog("Input Upper Class limit"));
                    limit[0] = null;
                    limit[1] = upper;
                    //classInterval.add(limit);
                }
            }
            
            //-------------------------
            //ask if open ended or not
            else if(i==number_of_classes-1){
                do{
                    n = OpenEndedOrNot();
                }while(n==-1);
                
                //Closed ended get first and second value
                if(n==1){
                    limit = inputClosedClassLimit();
//                    System.out.println("limit[0]" + limit[0]);
//                    System.out.println("limit[1]" + limit[1]);
                    //classInterval.add(limit);
                }
                
                //Open ended last
                else{
                    openLast = true;
                    lower = Double.valueOf(JOptionPane.showInputDialog("Input Lower Class limit"));
                    limit[0] = lower;
                    limit[1] = null;
                    //classInterval.add(limit);
                }
            }
            //-------------------------
            //ask item
            else{
                limit = inputClosedClassLimit();
//                System.out.println("limit[0]" + limit[0]);
//                System.out.println("limit[1]" + limit[1]);
                //classInterval.add(limit);
            }
            
            f = Double.valueOf(JOptionPane.showInputDialog("Input Frequency"));
            if(limit[0] == null || limit[1] == null)
                x = null;
            else
                x = (limit[0] + limit[1]) /2; 
            
            //<ClassLimit>, <f>, <x>, <fx>, <fx²>
            
            if(limit[0] == null){
//                System.out.println("≥" + limit[1].toString());
                row[0] = "≤" + limit[1].toString();
            }
            else if(limit[1] == null){
//                System.out.println("≤" + limit[0].toString());
                row[0] = "≥" + limit[0].toString();
            }
            else
                row[0] = limit[0].toString() + "-" + limit[1].toString();
            
            classInterval.add((Double[])limit.clone());
            //System.out.println("LOWER CLASS: "  + classInterval.get(0)[0]);
            
            row[1] = f;
            frequencies.add(f);
            
            row[2] = x;
            classMarks.add(x);
            
            if(f==null || x==null){
                row[3] = null;
                row[4] = null;
            }
            else{
                row[3] = f*x;
                row[4] = (f*x)*x;
            }
            tableModel.addRow(row);
            
            limit[0] = limit[1] = null;
        }
        
        if(openFirst == true || openLast == true){
            MeanCheckBox.setEnabled(false);
            MedianCheckBox.setEnabled(false);
        }
        
        stat = new CTGrouped(
            number_of_classes,
            classInterval, 
            frequencies,
            classMarks,
            openFirst,
            openLast,
            tableModel
        );
        
        SizeValue.setText(Double.toString(stat.get_n()));
        EfxValue.setText(Double.toString(stat.get_Efx()));
        EfxxValue.setText(Double.toString(stat.get_Efxx()));
        
        AddMoreValues.setEnabled(false);
        
    }//GEN-LAST:event_AddMoreValuesActionPerformed

    private void ComputeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComputeButtonActionPerformed
        // mean median mode
        
        if(MeanCheckBox.isSelected()){
            MeanValue.setText(Double.toString(stat.getMean()));
            VarianceValue.setText(Double.toString(stat.getVariance()));
            StdDevValue.setText(Double.toString(stat.getStdDev()));
        }
        if(MedianCheckBox.isSelected()){
            MedianValue.setText(Double.toString(stat.getMedian(number_of_classes)));
            IQRValue.setText(Double.toString(stat.getIQR()));
            QDValue.setText(String.valueOf(stat.getIQR()/2));
        }
        if(ModeCheckBox.isSelected()){
            ModeValue.setText(stat.getModalClass());
        }
    }//GEN-LAST:event_ComputeButtonActionPerformed

