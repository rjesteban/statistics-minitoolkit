    private static int OpenEndedOrNot(){
        Object[] options = {"Open Ended","Closed Ended"};
        int n = JOptionPane.showOptionDialog(new JFrame(),
            "Select kind of Class",
            "Kinds of Interval",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.INFORMATION_MESSAGE,
            null,     
            options,  
            null); 
        return n;
    }
    
    
    private static Double[] inputClosedClassLimit(){
        Double[] r = new Double[2];
        JTextField lowerLimitField = new JTextField();
        JTextField upperLimitField = new JTextField();
        Object[] message = {
            "LowerLimit:", lowerLimitField,
            "UpperLimit:", upperLimitField
        };

        int option = JOptionPane.showConfirmDialog(null, 
                message, 
                "Input Closed Class Limit",
                JOptionPane.OK_CANCEL_OPTION);
        
        if (option == JOptionPane.OK_OPTION) {
            r[0] = Double.valueOf(lowerLimitField.getText());
            r[1] = Double.valueOf(upperLimitField.getText());
            return r;
        } else {
            //System.out.println("Input canceled");
            return null;
        }
    }
    
    

